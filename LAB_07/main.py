import sys

try:
    n = int(input("Введіть розмір квадратної матриці: "))
except ValueError:
    print("Помилка: потрібно ввести число!")
    sys.exit(1)

symbol = input("Введіть символ-заповнювач: ")

if len(symbol) != 1:
    print("Потрібно ввести рівно один символ!")
    sys.exit(1)

# --- Створюємо справжній зубчастий масив ---
matrix = []
for i in range(n):
    row = [symbol] * (n - i)   # 5,4,3,2,1 ...
    matrix.append(row)

# --- Вивід правого верхнього трикутника ---
print("\nЗгенерований зубчастий масив:")
for i, row in enumerate(matrix):
    spaces = "  " * i          # друкуємо зміщення вправо
    print(spaces + " ".join(row))

print("\nРоботу програми завершено успішно.")