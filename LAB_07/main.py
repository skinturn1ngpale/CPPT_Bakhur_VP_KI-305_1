import sys

# Введення розміру квадратної матриці
try:
    n = int(input("Введіть розмір квадратної матриці: "))
except ValueError:
    print("Помилка: потрібно ввести число!")
    sys.exit(1)

# Введення символу-заповнювача
symbol = input("Введіть символ-заповнювач: ")

# Перевірка правильності введення символу
if len(symbol) == 0:
    print("Не введено символ-заповнювач!")
    sys.exit(1)
elif len(symbol) > 1:
    print("Введено забагато символів-заповнювачів!")
    sys.exit(1)

# Створення зубчастого списку (верхня права заштрихована область)
matrix = []

for i in range(n):
    # Кожен рядок буде містити n - i елементів
    row = []
    for j in range(n):
        if j >= i:  # умова для верхнього трикутника включно з діагоналлю
            row.append(symbol)
        else:
            row.append(" ")
    matrix.append(row)

# Виведення згенерованої матриці
print("\nЗгенерована матриця:")
for row in matrix:
    print(" ".join(row))

print("\nРоботу програми завершено успішно.")
